package chap06_1;

public class Book {
	// 필드
	private int bookNo;
	private String title;
	private int price;
	
	// 생성자
	Book (int bookNo,String title,int price){
		this.bookNo = bookNo;
		this.title = title;
		this.price = price;
	}

	public int getBookNo() {
		return bookNo;
	}

	public void setBookNo(int bookNo) {
		this.bookNo = bookNo;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}
	
	// 메소드
	
	
	
	
	
	
}
